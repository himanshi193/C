//TOPIC:SHIFT
//LEFT SHIFT OF ELEMENT by 1

#include<stdio.h>
int main(){
    int a=10;
    int result;
    result=a<<1 ;//left shift by 1
    printf("original value of a=%d\n",a);
    printf(" after left shift by 1=%d\n",result) ;
}

